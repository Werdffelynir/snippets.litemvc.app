<h4>Основное меню</h4>
<p> Образом дальнейшее развитие различных форм деятельности обеспечивает широкому кругу (специалистов) участие в
    формировании идей по выходу из сложившейся ситуации.</p>
<p> Таким образом сложившаяся структура организации требует от нас анализа направлений прогрессивного развития. </p>